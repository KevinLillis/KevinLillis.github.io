# lookAtStrings.py

def main():
    '''Main function for string example'''
    
    # Single Quotes
    message = 'Hello single'
    print(message)
    
    # Double Quotes
    message = "Hello Double"
    print(message)
    
    # Triple Quotes
    message = '''Hello Triple 1'''
    print(message)
    
    message = """Hello Triple 2"""
    print(message)
    
    # She said "I'm out of here" and left
    
    message = '''She said "I'm out of here" and left'''
    print(message)
    
    message = '''
    Line 1
    line 2
    '''
    
    print(message)
    
    message = 'She said "I\'m out of here" and left'
    print(message)
    
    message = "She said \"I'm out of here\" and left"
    print(message)
    
    message = 'Line 1\nLine2'
    print(message)
    
main()