'''
Draw Square, first version
'''

def drawSquare(cuff, sideLength):
    cuff.forward(sideLength)
    cuff.right(90)
    
    cuff.forward(sideLength)
    cuff.right(90)
    
    cuff.forward(sideLength)
    cuff.right(90)
    
    cuff.forward(sideLength)
    cuff.right(90)
