
public class Constants {
    public static void main(String[] args) {
        // Create x variable x
        int x = 4;
        System.out.println("x = " + x);

        // Since x is a variable we can change it
        x = x + 10;
        System.out.println("x = " + x);
        
        
        // Create a constant Y
        final int Y = 12;
        System.out.println("Y = " + Y);
        
        // Since Y is a constant it cannot be changed
        // Y = Y + 10; If we uncomment this line we get an error
        
    }
    
}
