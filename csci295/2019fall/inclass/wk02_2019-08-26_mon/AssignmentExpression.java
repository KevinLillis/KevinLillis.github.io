public class AssignmentExpression {
    public static void main(String[] args) {
        double x;
        System.out.println(x = 99.99);
        
        System.out.println("x = " + x);
        
        double a, b, c, d, e, f;
        
        a = b = c = d = e = f = 12.1;
        
        System.out.println("c = "  + c);
    }
}
